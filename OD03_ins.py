def insertion_sort(arr):
    """
    Сортировка вставками.
    Последовательно берём элементы массива и вставляем их в отсортированную часть слева.
    """
    for i in range(1, len(arr)):
        key = arr[i]  # текущий элемент, который нужно вставить
        j = i - 1

        # пока есть элементы слева и они больше ключа — сдвигаем их вправо
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]  # сдвигаем элемент вправо
            j -= 1

        # вставляем ключ на нужное место
        arr[j + 1] = key

    return arr


print(insertion_sort([5, 2, 9, 1, 5, 6]))  # [1, 2, 5, 5, 6, 9]